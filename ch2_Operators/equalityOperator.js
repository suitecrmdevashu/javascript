//Equality Operator
// ===, ==, !==, !=

let price = 10;
console.log(price === 10); //Compare with datatype (equal value and equal type)
console.log(price == 10); //compare with only value (equal to)

console.log(price !== 10); // Compare value and datatype (not equal value or not equal type). 
console.log(price != 10); // Compare value only (not equal).


